public class FizzBuzz {
    public static void main(String[] args) {
        // Print FizzBuzz from 1 to 42
        for (int i = 1; i <= 42; i++) {
            if (i % 3 == 0 && i % 5 == 0) {
                System.out.println("FizzBuzz");
            } else if (i % 3 == 0) {
                System.out.println("Fizz");
            } else if (i % 5 == 0) {
                System.out.println("Buzz");
            } else {
                System.out.println(i);
            }
        }
    }
}